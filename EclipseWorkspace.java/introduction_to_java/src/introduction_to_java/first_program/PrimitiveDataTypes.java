package introduction_to_java.first_program;

public class PrimitiveDataTypes {
	
	public static void main(String[]args) {
		//DAta types -- 1.primitive datatype and 2.non-primitive
        /*
         * primitive datatypes
         * 1.byte(default value=0)
         * 2.short (default value=0)
         * 3.int (default value=0)
         * 4.long (default value=0l)
         * 5. float- ex= 9.8f (default value= 0.0f)
         * 6.double (default value =0.0)
         * 7.char (default='')
         * 8.boolean(default value=false)
         */
		
		//DECLARATION OF VARIABLES
		/*
		 * variable is a container used to store a value based on the datatype
		 * <datatype> varible_name;
		 */
		
		byte age;
		// everone is samml letter like byte long int etc..
		
		//Initialization: variable_name=value;
		age=20;
		
		//OR
		//<datatype> variable_name=value
		byte age1 = 34;
		System.out.println(age);
		System.out.println(age1);
		
		
		long one=34;
		System.out.println(one);
		
		short two=23;
		System.out.println(two);
		
		int three=25;
		System.out.println(three);
		
		float four=6.5f;
		System.out.println(four);
		
		double five=54;
		System.out.println(five);
		
		char six=675;
		System.out.println(six);
		
		boolean sev=true;
		System.out.println(sev);
		
		//scanner-used to take a userinput
		
		
		
		
	}

}
