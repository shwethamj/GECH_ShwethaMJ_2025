package javaInterview;

public class Tycasting {

//	1. What happen if we typecaste the double value to int value with example ?
	public static void main(String[] args) {

		// here explicit type casting will done
		double a = 20.5;
		int b = (int) a;
		System.out.println("Double value of a : " + a);
		System.out.println("int value of b : " + b);

	}
}
