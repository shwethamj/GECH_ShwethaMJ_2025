		/*
		 * METHODS IN STRING BUFFER:
		 * ===========================
		 
		 * 1.append(String str) 
		  			==> Adds (appends) text to the end of the buffer.
		 
		 * 2.insert(int offset, String str)
		  			==> Inserts text at the specified index.
		 
		 * 3. replace(int start, int end, String str)
		  			==> Replaces characters between start (inclusive) and end (exclusive) with the given string.
		 
		 * 4. delete(int start, int end) 
		 			==> Deletes characters from start (inclusive) to end (exclusive).
		 
		 * 5. deleteCharAt(int index)  
		 		    ==> Deletes the character at the specified index.
		 
		 * 6. reverse() 
		 			==>Reverses the characters in the buffer.
		 			
		 * 7. capacity()
		  			==>Returns the current capacity of the buffer (how many characters it can hold before resizing).
		
		 * 8. length() 
		 			==>Returns the number of characters currently in the buffer.
		 
		 * 9. charAt(int index)
		  			==>Returns the character at a specific index.
		  			
		 * 10. setCharAt(int index, char ch) 
		 			==>Sets (modifies) the character at the specified index.
		 			
		 * 11. toString() 
		 			==> Converts StringBuffer to a String.
	
		 * 
		 */
