package stringBuffer_in_java;

public class ConstructorInStringBuffer {

	/*
	 *  1. StringBuffer() :
			Creates an empty string buffer with default capacity (16 characters).
		
		2. StringBuffer(int capacity) :
			Creates an empty buffer with the specified capacity.
		
		3. StringBuffer(String str) :
			Creates a buffer initialized with the specified string.
			Capacity = str.length() + 16
			
		4. StringBuffer(CharSequence seq):
			Initializes with a character sequence (like StringBuilder, String).
	 */
}
